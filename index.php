        </p>
        <h2>Tipos de cookies</h2>
        <p>Según quien sea la entidad que gestione el dominio desde donde se envían las cookies y trate los datos que se
            obtengan se pueden distinguir dos tipos:</p>
        <ul>
            <li>Cookies propias:
                <br> Aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio gestionado por el propio
                editor y desde el que se presta el servicio solicitado por el usuario.</li>
            <li>Cookies de terceros:
                <br> Aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio que no es gestionado
                por el editor, sino por otra entidad que trata los datos obtenidos través de las cookies.</li>
        </ul>
        <p>En el caso de que las cookies sean instaladas desde un equipo o dominio gestionado por el propio editor pero
            la información que se recoja mediante éstas sea gestionada por un tercero, no pueden ser consideradas como
            cookies propias.
        </p>

        <p>Existe también una segunda clasificación según el plazo de tiempo que permanecen almacenadas en el navegador
            del cliente, pudiendo tratarse de:</p>

        <ul>
            <li>Cookies de sesión:
                <br> Diseñadas para recabar y almacenar datos mientras el usuario accede a una página web. Se suelen emplear
                para almacenar información que solo interesa conservar para la prestación del servicio solicitado por
                el usuario en una sola ocasión (p.e. una lista de productos adquiridos).</li>
            <li>Cookies persistentes:
                <br> Los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante un periodo definido
                por el responsable de la cookie, y que puede ir de unos minutos a varios años.</li>
        </ul>
        <p>Por último, existe otra clasificación con seis tipos de cookies según la finalidad para la que se traten los
            datos obtenidos:
        </p>
        <ul>
            <li>Cookies técnicas:
                <br> Aquellas que permiten al usuario la navegación a través de una página web, plataforma o aplicación y
                la utilización de las diferentes opciones o servicios que en ella existan como, por ejemplo, controlar
